    private void jbGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbGuardarActionPerformed
        // TODO add your handling code here:
        long dni = Integer.parseInt(jtDni.getText());
        String nombre = jtNombre.getText();
        String apellido = jtApellido.getText();
        String ciudad = jtCiudad.getText();
        String domicilio = jtDomicilio.getText();
        String telefono = jtTelefono.getText();
        
        Cliente c = new Cliente(dni, nombre, apellido, ciudad, domicilio);
        String tel = new String(telefono);
        
        Directorio dir = FormularioClientes.getDirectorio();
        if (dir.agregarCliente(tel, c)){
         JOptionPane.showMessageDialog(this, "Cliente Agregado"); 
        }
        else {
            JOptionPane.showMessageDialog(this, "No se puede agregar");
        }
        
    }//GEN-LAST:event_jbGuardarActionPerformed

